//билет 46. Основы Vue3. Блоки data, methods, compudet, watch, mounted
// Давай разберем основные блоки Vue 3: data, methods, computed, watch и mounted.
//  Эти блоки помогают организовать код и управлять состоянием приложения.

// 1. Блок data
// Блок data используется для хранения состояния приложения. В этом блоке определяются переменные,
//  которые будут использоваться в приложении. Эти переменные могут быть изменены, и Vue автоматически
//   обновит интерфейс, когда они изменятся.
// 2. Блок methods
// Блок methods содержит функции, которые могут быть вызваны из шаблона или других методов. Эти функции  обычно используются для обработки событий, таких как нажатия кнопок, и для выполнения действий, которые изменяют состояние приложения.
// 3. Блок computed
// Блок computed используется для создания вычисляемых свойств. Эти свойства зависят от других данных и 
// автоматически обновляются, когда изменяются исходные данные. Вычисляемые свойства полезны для выполнения
//  сложных вычислений или преобразований данных.
// 4. Блок watch
// Блок watch позволяет следить за изменениями переменных и выполнять действия, когда они изменяются. Это 
// полезно для выполнения асинхронных операций, таких как запросы к серверу, или для валидации данных.
// 5. Блок mounted
// Блок mounted — это жизненный цикл компонента, который срабатывает после того, как компонент был добавлен
//  в DOM. Это хорошее место для выполнения действий, которые требуют доступа к элементам DOM, таких как
//   инициализация сторонних библиотек или выполнение запросов к серверу


// Блок	Аналог на кухне	Как работает
// data	Ингредиенты в холодильнике	Хранит исходные данные
// methods	Нож, блендер, плита	Инструменты для действий
// computed	Автоматическая хлебопечка	Преобразует данные (мука → хлеб)
// watch	Детектор дыма	Следит за изменениями
// mounted	Повар, который пришел на работу	Запускает процессы при готовности

const { createApp } = Vue;

const App = {
    // 1. Data - храним данные
    data() {
        return {
            message: "Памагите",//строка приветсвтие
            inputText: ""//текст из поля ввода
        }
    },
    
    // 2. Computed - вычисляемые свойства
    computed: {
        textLength() {//обновляется при изменении инпуттекст
            return this.inputText.length;
        }
    },
    
    // 3. Methods - методы
    methods: {
        showAlert() {//показывает введеный текст
            alert(`Вы ввели: ${this.inputText}`);
        }
    },
    
    // 4. Watch - следим за изменениями
    watch: {//отслеживает изменения
        inputText(newVal) {//выводим текст в консоль
            console.log(`Текст изменен на: ${newVal}`);
        }
    },
    
    // 5. Mounted - выполняется при загрузке
    mounted() {
        console.log("Приложение загружено!");//выводим в консольЮ выполняется только один раз
    }
}

const app = createApp(App);
app.mount('#app');


