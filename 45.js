// Определяем объект приложения
const App = {
    data() {
        return {
            title: "Мое Vue приложение",
            name: "",
            counter: 0,
            link: {
                name: "Vue.js",
                url: "https://vuejs.org",
                title: "Официальный сайт Vue"
            }
        }
    }
}
// Создаем и монтируем приложение
const app = Vue.createApp(App);
app.mount('#app');



